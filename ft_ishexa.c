#include "libft.h"

t_bool		ft_ishexa(int c)
{
	if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'f') ||
	(c >= 'A' && c <= 'F'))
		return (TRUE);
	return (FALSE);
}
